//6. Program for relational operators (<,<=, >, >=)
package com.operators;

public class Six {

	public static void main(String[] args) {
		int a = 10;
		int b =20;
		System.out.println("a < b ?: "+ (a<b));
		System.out.println("a <= b ?: "+ (a<=b));
		System.out.println("a > b ?: "+ (a>b));
		System.out.println("a >= b ?: "+ (a>=b));

	}

}
