//5. Programs on Logical AND,OR operator and Logical NOT
package com.operators;

public class Five {
	public static void main(String[] args) {

		boolean a = true;
		boolean b = false;
		System.out.println("Logical AND Operator: "+ (a && b));
		System.out.println("Logical OR Operator: "+ (a || b));
		System.out.println("Logical NOT Operator on b: " + (!b));
		
	}

}
